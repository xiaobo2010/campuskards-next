export function getRandomTips(unitType: string | null | undefined, cardType: string | null | undefined, count: number = 2): string[] {
  let pool: string[];

  if (cardType === "unit" || cardType === "character") {
    if (unitType && combatTips[unitType]) {
      pool = combatTips[unitType];
    } else {
      pool = combatTips.generic_unit;
    }
  } else if (cardType === "command" || cardType === "event") {
    pool = combatTips.command;
  } else if (cardType === "counter" || cardType === "snitch") {
    pool = combatTips.counter;
  } else if (cardType === "buff") {
    pool = combatTips.buff;
  } else {
    pool = combatTips.general;
  }

  // Shuffle and pick `count` tips
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
